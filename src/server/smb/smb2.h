struct smb2_header {
    uint8_t  protocol_id[4];
    uint16_t struct_size;
    uint16_t credit_charge;
    uint32_t status;
    uint16_t command;
    uint16_t credit_request_response;
    uint32_t flags;
    uint32_t next_command;
    uint64_t message_id;
    union {
        struct {
            uint64_t async_id;
        } async;
        struct {
            uint32_t process_id;
            uint32_t tree_id;
        } sync;
    };
    uint64_t session_id;
    uint8_t  signature[16];
};

